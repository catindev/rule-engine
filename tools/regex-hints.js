/**
 * regex-hints.js
 *
 * Таблица человекочитаемых описаний для известных regex-паттернов.
 * Подход: сопоставление строки паттерна → русское описание.
 *
 * Почему не парсер:
 *   Полный набор продовых паттернов — 7 штук, все известны заранее.
 *   Универсальный парсер regex → русский текст не существует в надёжном виде.
 *   Таблица прозрачна, легко расширяется, не ломается на новых паттернах.
 *
 * При добавлении нового паттерна в правила — добавить строку в HINTS.
 * Порядок: более специфичные паттерны выше менее специфичных.
 */

// Каждая запись: { match: fn(str) => bool, hint: string }
const HINTS = [
  // --- карты (специфичнее дат — ставим выше) ---
  {
    // ^(0[1-9]|1[0-2])\/(2[4-9]|[3-9][0-9])$  — MM/YY, год не в прошлом
    match: (s) => s.includes('2[4-9]') || s.includes('[3-9][0-9]'),
    hint: 'формат: MM/YY, год ≥ 24 (не истёкший)',
  },
  {
    // ^(0[1-9]|1[0-2])\/[0-9]{2}$  — MM/YY любой
    match: (s) => s.includes('0[1-9]') && s.includes('1[0-2]') && s.includes('[0-9]{2}'),
    hint: 'формат: MM/YY (срок карты)',
  },
  {
    // ^\d{16}$
    match: (s) => s === '^\\d{16}$',
    hint: 'формат: 16 цифр (номер карты)',
  },

  // --- дата ISO ---
  {
    // ^\d{4}-\d{2}-\d{2}$
    match: (s) => s === '^\\d{4}-\\d{2}-\\d{2}$',
    hint: 'формат: YYYY-MM-DD',
  },

  // --- коды стран ---
  {
    // ^[A-Z]{2}$
    match: (s) => s === '^[A-Z]{2}$',
    hint: 'формат: код страны ISO alpha-2 (RU, US…)',
  },

  // --- телефон E.164 ---
  {
    // ^\+[1-9]\d{7,14}$
    match: (s) => s === '^\\+[1-9]\\d{7,14}$',
    hint: 'формат: телефон E.164 (+79001234567)',
  },

  // --- email ---
  {
    // ^[^@\s]+@[^@\s]+\.[^@\s]+$
    match: (s) => s.includes('@') && s.includes('[^@') && s.includes('\\s]'),
    hint: 'формат: email (user@domain.ext)',
  },
];

/**
 * Возвращает человекочитаемое описание regex или null если паттерн неизвестен.
 * @param {string} regexStr — строковое значение поля value из правила
 * @returns {string|null}
 */
function regexHint(regexStr) {
  if (!regexStr) return null;
  for (const { match, hint } of HINTS) {
    if (match(regexStr)) return hint;
  }
  return null;
}

module.exports = { regexHint };
